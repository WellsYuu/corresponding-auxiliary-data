import java.util.ArrayList;
import java.util.List;

public class Crc16Sharding {
    List<RedisClient2> pool;

    public Crc16Sharding(List<RedisClient2> pool) {
        this.pool = pool;
    }

    public RedisClient2 crc16hash(String key){
         int num=Math.abs(key.hashCode())%pool.size();
        return pool.get(num);

    }



}
